package pl.sda.zdjavapol75.typyDanych;

public class DemoOperatorow {
    public static void main(String[] args) {

        int wiek = 50;
        System.out.println("Wiek to: " + wiek);

        wiek++; // jest to operacja tozsama z linia 12:
        System.out.println("Wiek to: " + wiek);

        wiek = wiek - 1;
        System.out.println("Wiek to: " + wiek);

        wiek--;
        wiek++;
        --wiek;
        ++wiek;

        wiek = 10;


        System.out.println(wiek++);

        System.out.println(wiek);


        int liczba = 100;

        liczba = 5; // jest tozsamy z zapisem: liczba = liczba + 5;





    }
}
